function onload() { 
document.getElementById("radio1").style.display = "block";
document.getElementById("radio2").style.display = "none";
document.getElementById("radio3").style.display = "none";
document.getElementById("radio4").style.display = "none";
document.getElementById("radio5").style.display = "none";
document.getElementById("radio6").style.display = "none";

}
 




function mostrarcomienzo(){
element = document.getElementById("radio1");
        check = document.getElementById("uno");
	element2 = document.getElementById("radio2");
	element3 = document.getElementById("radio3");
	element4 = document.getElementById("radio4");
	element5 = document.getElementById("radio5");
	element6 = document.getElementById("radio6");
        if (check.checked) {
            element.style.display='block';
	    element2.style.display='none';
	    element3.style.display='none';
	    element4.style.display='none';
	    element5.style.display='none';
	    element6.style.display='none';
        }
        else {
            element.style.display='block';
	    element2.style.display='none';
	    element3.style.display='none';
	    element4.style.display='none';
	    element5.style.display='none';
	    element6.style.display='none';
        }

}

